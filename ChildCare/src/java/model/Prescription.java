package model;

/**
 *
 * @author admin
 */
public class Prescription {
    private int prescriptionId, examinationId, doctorId, customerId, medicineId;
    private String dosage, note;
    private float totalCost;

    public Prescription() {
    }

    public Prescription(int prescriptionId, int examinationId, int doctorId, int customerId, int medicineId, String dosage, String note, float totalCost) {
        this.prescriptionId = prescriptionId;
        this.examinationId = examinationId;
        this.doctorId = doctorId;
        this.customerId = customerId;
        this.medicineId = medicineId;
        this.dosage = dosage;
        this.note = note;
        this.totalCost = totalCost;
    }

    public int getPrescriptionId() {
        return prescriptionId;
    }

    public void setPrescriptionId(int prescriptionId) {
        this.prescriptionId = prescriptionId;
    }

    public int getExaminationId() {
        return examinationId;
    }

    public void setExaminationId(int examinationId) {
        this.examinationId = examinationId;
    }

    public int getDoctorId() {
        return doctorId;
    }

    public void setDoctorId(int doctorId) {
        this.doctorId = doctorId;
    }

    public int getCustomerId() {
        return customerId;
    }

    public void setCustomerId(int customerId) {
        this.customerId = customerId;
    }

    public int getMedicineId() {
        return medicineId;
    }

    public void setMedicineId(int medicineId) {
        this.medicineId = medicineId;
    }

    public String getDosage() {
        return dosage;
    }

    public void setDosage(String dosage) {
        this.dosage = dosage;
    }

    public String getNote() {
        return note;
    }

    public void setNote(String note) {
        this.note = note;
    }

    public float getTotalCost() {
        return totalCost;
    }

    public void setTotalCost(float totalCost) {
        this.totalCost = totalCost;
    }
    
    
}
