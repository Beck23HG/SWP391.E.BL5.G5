package model;

import java.util.Date;

/**
 *
 * @author admin
 */
public class Blog {
    private int blogId, personId, categoryId;
    private String title, content, image, description;
    private Date created_Date;
    private Person person;
    private Category category;

    public Blog() {
    }

    public Blog(int blogId, int personId, int categoryId, String title, String content, String image, String description, Date created_Date, Person person, Category category) {
        this.blogId = blogId;
        this.personId = personId;
        this.categoryId = categoryId;
        this.title = title;
        this.content = content;
        this.image = image;
        this.description = description;
        this.created_Date = created_Date;
        this.person = person;
        this.category = category;
    }

    public int getBlogId() {
        return blogId;
    }

    public void setBlogId(int blogId) {
        this.blogId = blogId;
    }

    public int getPersonId() {
        return personId;
    }

    public void setPersonId(int personId) {
        this.personId = personId;
    }

    public int getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(int categoryId) {
        this.categoryId = categoryId;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Date getCreated_Date() {
        return created_Date;
    }

    public void setCreated_Date(Date created_Date) {
        this.created_Date = created_Date;
    }

    public Person getPerson() {
        return person;
    }

    public void setPerson(Person person) {
        this.person = person;
    }

    public Category getCategory() {
        return category;
    }

    public void setCategory(Category category) {
        this.category = category;
    }

   

   
    
    
}
